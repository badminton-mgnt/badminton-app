export const translate = (language, key, params = {}) => {
  const langDict = translations[language] || translations[DEFAULT_LANGUAGE]
  const fallbackDict = translations[DEFAULT_LANGUAGE]
  const template = langDict?.[key] ?? fallbackDict?.[key] ?? key

  return String(template).replace(/\{(\w+)\}/g, (_, token) => String(params[token] ?? ''))
}
